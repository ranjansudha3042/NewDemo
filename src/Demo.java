

public class Demo {

	public static void main(String[] args) {
		 
		                              ////  Int
	
		int a= 20;
		int b= 40;
		int num=a+b;
		System.out.println("The sum is = " + num);
		
		                             ////  String
		
		String sudh= "My FirstName is Sudhanshu";
		String ranj= "My Surname is Ranjan";
		System.out.println("The length is = " + sudh.length());
		System.out.println(sudh.toUpperCase());
		System.out.println(sudh.toLowerCase());
		System.out.println(sudh.indexOf("Sudhanshu"));
		System.out.println(sudh.concat(ranj));
		
		                            ////  Math function
		
		
		System.out.println(Math.max(a, b));
		System.out.println(Math.abs(-10));
		
		                           //// Boolean
		
		System.out.println(a>b);   

	}

}
